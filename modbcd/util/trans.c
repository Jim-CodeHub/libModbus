/**
 *	@brief	    Trans 8bit Hex value to two Ascii characters 
 *	@param[in]  Hex 
 *	@param[out] ascii	
 *	@return		None	
 **/
void hexToAscii(unsigned char Hex, char ascii[2])
{
	char H = Hex >> 4  ;
	char L = Hex & 0X0F;

	*ascii = H + ((H < 10)?48:55); ascii++;
	*ascii = L + ((L < 10)?48:55);
	
	return;
}

